#include "libft.h"

int		ft_iscntrl(int c)
{
	unsigned char p;

	p = (unsigned char)c;
	return ((p >= 000 && p <= 007) || (p >= 010 && p <= 017) ||
			(p >= 020 && p <= 027) || (p >= 030 && p <= 037) || p == 0177);
}
